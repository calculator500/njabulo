#include "print_x.c"
#include "print_y.c"

void	rush00(int x, int y)
{
	if (x >= 1)
		print_x(x, 'o', '-', 'o');
	print_y(x, y, '|');
	if (y >= 2)
		print_x(x, 'o', '-', 'o');
}
