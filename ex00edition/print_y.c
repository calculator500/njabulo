void	print_y(int x, int y, int wall)
{
	int i;

	while (y-- > 2)
	{
		ft_putchar(wall);
		i = 2;
		while (x > i++)
			ft_putchar(' ');
		ft_putchar(wall);
		ft_putchar('\n');
	}
}
